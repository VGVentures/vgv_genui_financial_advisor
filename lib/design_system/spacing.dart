abstract class Spacing {
  static const double _spaceUnit = 4;

  /// xxs spacing value (4pt)
  static const double xxs = _spaceUnit;

  /// xs spacing value (8pt)
  static const double xs = _spaceUnit * 2;

  /// sm spacing value (12pt)
  static const double sm = _spaceUnit * 3;

  /// md spacing value (16pt)
  static const double md = _spaceUnit * 4;

  /// lg spacing value (20pt)
  static const double lg = _spaceUnit * 5;

  /// xl spacing value (24pt)
  static const double xl = _spaceUnit * 6;

  /// xxl spacing value (28pt)
  static const double xxl = _spaceUnit * 7;

  /// xxxl spacing value (32pt)
  static const double xxxl = _spaceUnit * 8;
}
